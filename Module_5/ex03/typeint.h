#ifndef TYPEINT_H
# define TYPEINT_H

/* C++98 doesn't have those standard so i'll just import them myself */

/* Signed */
typedef char m_int8_t;
typedef short int m_int16_t;
typedef int m_int32_t;
typedef long long m_int64_t;

/* Unsigned */
typedef unsigned char m_uint8_t;
typedef unsigned short int m_uint16_t;
typedef unsigned int m_uint32_t;
typedef unsigned long long m_uint64_t;

#endif